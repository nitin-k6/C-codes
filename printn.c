#include<stdio.h>
// To print \n you simply need to write printf("\\n");
//  If you want to go to a newline after printing you need to write printf("\\n\n");
//   Basically when we use printf("\\n") then "\\" produces "\" and then "n" is recognized as an normal ordinary symbol.
int main(){
    int i=0;
    printf("\\n");
    return 0;
}